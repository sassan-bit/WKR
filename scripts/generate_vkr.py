def set_page_margins(doc):
    """ГОСТ: поля 30мм (лево), 15мм (право), 20мм (верх/низ)."""
    for section in doc.sections:
        section.page_width  = Mm(210)
        section.page_height = Mm(297)
        section.left_margin   = Mm(30)
        section.right_margin  = Mm(15)
        section.top_margin    = Mm(20)
        section.bottom_margin = Mm(20)
        section.header_distance = Mm(10)
        section.footer_distance = Mm(10)


def add_page_numbers(doc):
    """Добавляет номер страницы по центру внизу."""
    for section in doc.sections:
        footer = section.footer
        p = footer.paragraphs[0] if footer.paragraphs else footer.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.clear()
        run = p.add_run()
        fldChar1 = OxmlElement('w:fldChar')
        fldChar1.set(qn('w:fldCharType'), 'begin')
        instrText = OxmlElement('w:instrText')
        instrText.text = 'PAGE'
        fldChar2 = OxmlElement('w:fldChar')
        fldChar2.set(qn('w:fldCharType'), 'end')
        run._r.append(fldChar1)
        run._r.append(instrText)
        run._r.append(fldChar2)
        rpr = run._r.get_or_add_rPr()
        rFonts = OxmlElement('w:rFonts')
        rFonts.set(qn('w:ascii'), 'Times New Roman')
        rFonts.set(qn('w:hAnsi'), 'Times New Roman')
        rpr.append(rFonts)
        sz = OxmlElement('w:sz')
        sz.set(qn('w:val'), '28')
        rpr.append(sz)


def setup_styles(doc):
    """Настраивает стили документа под ГОСТ."""
    styles = doc.styles

    # Normal — основной стиль
    normal = styles['Normal']
    normal.font.name = 'Times New Roman'
    normal.font.size = Pt(14)
    normal._element.rPr.rFonts.set(qn('w:eastAsia'), 'Times New Roman')
    pf = normal.paragraph_format
    pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    pf.first_line_indent = Cm(1.25)
    pf.space_before = Pt(0)
    pf.space_after  = Pt(0)
    pf.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY

    # Heading 1 — главы (1 НАЗВАНИЕ)
    h1 = styles['Heading 1']
    h1.font.name = 'Times New Roman'
    h1.font.size = Pt(14)
    h1.font.bold = True
    h1.font.color.rgb = RGBColor(0, 0, 0)
    h1._element.rPr.rFonts.set(qn('w:eastAsia'), 'Times New Roman')
    h1pf = h1.paragraph_format
    h1pf.alignment = WD_ALIGN_PARAGRAPH.CENTER
    h1pf.first_line_indent = Cm(0)
    h1pf.space_before = Pt(12)
    h1pf.space_after  = Pt(6)
    h1pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    h1pf.page_break_before = True
    h1pf.keep_with_next = True

    # Heading 2 — подразделы (1.1. Название)
    h2 = styles['Heading 2']
    h2.font.name = 'Times New Roman'
    h2.font.size = Pt(14)
    h2.font.bold = True
    h2.font.color.rgb = RGBColor(0, 0, 0)
    h2._element.rPr.rFonts.set(qn('w:eastAsia'), 'Times New Roman')
    h2pf = h2.paragraph_format
    h2pf.alignment = WD_ALIGN_PARAGRAPH.LEFT
    h2pf.first_line_indent = Cm(1.25)
    h2pf.space_before = Pt(6)
    h2pf.space_after  = Pt(3)
    h2pf.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE
    h2pf.page_break_before = False
    h2pf.keep_with_next = True

    # Caption style for figures/tables
    if 'Caption' not in [s.name for s in styles]:
        cap = styles.add_style('Caption', WD_STYLE_TYPE.PARAGRAPH)
    else:
        cap = styles['Caption']
    cap.font.name = 'Times New Roman'
    cap.font.size = Pt(14)
    cap.paragraph_format.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.first_line_indent = Cm(0)
    cap.paragraph_format.space_before = Pt(3)
    cap.paragraph_format.space_after  = Pt(6)
    cap.paragraph_format.line_spacing_rule = WD_LINE_SPACING.ONE_POINT_FIVE


