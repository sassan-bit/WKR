def fig_placeholder(doc, num, caption):
    """Вставляет рисунок если файл есть, иначе красную плашку."""
    fname = _FIG_MAP.get(num)
    if fname:
        fpath = FIG_DIR / fname
        if fpath.exists():
            # Determine width based on figure type
            if any(x in fname for x in ['flowchart', 'pe_structure']):
                width = Cm(12)
            else:
                width = Cm(15)
            p = doc.add_paragraph(style='Normal')
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            p.paragraph_format.first_line_indent = Cm(0)
            run = p.add_run()
            run.add_picture(str(fpath), width=width)
        else:
            _red_placeholder(doc, num)
    else:
        _red_placeholder(doc, num)

    cap = doc.add_paragraph(style='Normal')
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.first_line_indent = Cm(0)
    cap.add_run(f'Рисунок {num} – {caption}')
    return cap


def _red_placeholder(doc, num):
    p = doc.add_paragraph(style='Normal')
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.first_line_indent = Cm(0)
    run = p.add_run(f'[РИСУНОК {num} — ВСТАВИТЬ СКРИНШОТ/СХЕМУ]')
    run.font.color.rgb = RGBColor(0xCC, 0x00, 0x00)
    run.font.bold = True


